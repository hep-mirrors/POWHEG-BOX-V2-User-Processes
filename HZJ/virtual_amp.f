      function epsilon(a,b,c,d)
      implicit none
      real *8 a(4),b(4),c(4),d(4),ris
      integer i,j,k,l
      real *8 mat(4,4),epsilon
      real*8 getdet
      external getdet
      do i=1,4
         mat(1,i) = a(i)
         mat(2,i) = b(i)
         mat(3,i) = c(i)
         mat(4,i) = d(i)      
      enddo
      epsilon = -getdet(mat,4)
      end


      real *8 function getdet(a,n)
      implicit real *8 (a-h,o-z)
      real *8 elem(n,n),a(n,n)
      real *8 m,temp
      integer i,j,k,l
      logical detexists
      do i=1,n
         do j=1,n
            elem(i,j) = a(i,j)
         enddo
      enddo
      detexists = .TRUE.
      l=1
      do k=1,n-1
         if (dabs(elem(k,k)).le.1.0d-20) then
            detexists = .FALSE.
            do i=k+1,n
               if (elem(i,k).ne.0.0) then
                  do j=1,n
                     temp = elem(i,j)
                     elem(i,j) = elem(k,j)
                     elem(k,j) = temp
                  enddo
                  detexists = .TRUE.
                  l=-l
                  exit
               endif
            enddo
            if (detexists.eqv..FALSE.) then
               getdet = 0
               return
            endif
         endif
         do j=k+1,n
            m=elem(j,k)/elem(k,k)
            do i=k+1,n
               elem(j,i) = elem(j,i) - m*elem(k,i)
            enddo
         enddo
      enddo
      getdet = l
      do i=1,n
         getdet = getdet*elem(i,i)
      enddo
      end
