        average_factor_ppttj_dec_nenmxexmbbxgggg_1 &
        * average_factors_ppttj_dec_nenmxexmbbxgggg_1( &
        external_perm_ppttj_dec_nenmxexmbbxgggg_1(i))
    end do
    ! Symmetry factor for outgoing particles
