      do i = 1, 10
        average_factor_ppttj_dec_nenmxexmbbxgggg_1 = &
          average_factor_ppttj_dec_nenmxexmbbxgggg_1 &
          * factorial(count(particle_types_perm_ppttj_dec_nenmxexmbbxgggg_1(n_scatt+1:10) == i))
      end do
    end if
  end subroutine set_permutation


