          ii = external_perm_ppttj_dec_nenmxexmbbxgggg_1(j)
          jj = external_perm_ppttj_dec_nenmxexmbbxgggg_1(i)
        end if
        extcomb_perm_ppttj_dec_nenmxexmbbxgggg_1((i*(i-1))/2 + j) = (ii*(ii-1))/2 + jj
      end do
    end do
    ! Colour and helicity average factor
