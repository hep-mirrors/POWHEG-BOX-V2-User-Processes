  integer,           save :: exthel(512,9)
  integer,           save :: firstpol(9)

  contains

! **********************************************************************
subroutine init_heltables
! **********************************************************************
  use ol_helicity_init, only: heltable
  implicit none

  ! I/O helicity tables for vertices, propagators and contractions;
  ! helicity table for a vertex call: n_in/n_out are the number helicity states of the incoming/outgoing wave functions
  ! call heltable([<n_in1>, <n_in2>, ..., <n_out>], n, t)
  ! propagators only need the number of helicity configurations which is equal for the incoming and outgoing wave function
  ! n = <n>
  call heltable([2,2,4], n3(:,1), t3x4(:,:,1))
  call heltable([2,2,4], n3(:,2), t3x4(:,:,2))
  call heltable([2,2,4], n3(:,3), t3x4(:,:,3))
  n2(1) = 4
  n2(2) = 4
  call heltable([4,2,8], n3(:,4), t3x8(:,:,1))
  call heltable([2,4,8], n3(:,5), t3x8(:,:,2))
  call heltable([4,2,8], n3(:,6), t3x8(:,:,3))
  n2(3) = 8
  n2(4) = 8
  call heltable([8,8,64], n3(:,7), t3x64(:,:,1))
  call heltable([2,8,16], n3(:,8), t3x16(:,:,1))
  call heltable([8,4,32], n3(:,9), t3x32(:,:,1))
  n2(5) = 16
  call heltable([8,2,16], n3(:,10), t3x16(:,:,2))
  call heltable([4,8,32], n3(:,11), t3x32(:,:,2))
  n2(6) = 16
  call heltable([2,2,4], n3(:,12), t3x4(:,:,4))
  n2(7) = 4
  call heltable([4,2,8], n3(:,13), t3x8(:,:,4))
  call heltable([8,8,64], n3(:,14), t3x64(:,:,2))
  call heltable([2,2,4], n3(:,15), t3x4(:,:,5))
  n2(8) = 4
  call heltable([2,4,8], n3(:,16), t3x8(:,:,5))
  call heltable([8,8,64], n3(:,17), t3x64(:,:,3))
  call heltable([8,64,512], n3(:,18), t3x512(:,:,1))
  call heltable([32,16,512], n3(:,19), t3x512(:,:,2))
  call heltable([32,16,512], n3(:,20), t3x512(:,:,3))
  call heltable([8,64,512], n3(:,21), t3x512(:,:,4))
  call heltable([8,64,512], n3(:,22), t3x512(:,:,5))

  heltables_not_init = .false.

end subroutine init_heltables

